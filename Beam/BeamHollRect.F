      subroutine BeamHollRect (w1,w2,t1,t2,t3,t4,rData,
     &                         ElemConn,numNodes,numElems)
#include "impcom.inc"
      double precision
     &        rData(*),w1,w2,t1,t2,t3,t4, ZERO, HALF, FOURTH, ONE,
     &        PosIncr
      integer ElemConn(9,*),numElems, numNodes, NodeThruThick,
     &        k1, k2, ii, ki, kj,
     &        ElemPerSide, NodePerSide, ElemThruThick,
     &        NodePerSide1, numElems1

      parameter (NodePerSide1 = 8+1, numElems1 = 12)
      integer    ElemConn1(8+1,12)
      double precision rDataz(8+1)
      parameter (ZERO=0.0d0, HALF=0.5d0, FOURTH=0.25d0, ONE=1.0d0)

c --- subsection numbering
c           ____________________
c           | 3 |    8     | 6 |
c           |---|----------|---|
c           |   |          |   |
c           | 2 |          | 5 |
c           |   |          |   |
c           |___|__________|___|
c           | 1 |    7     | 4 |
c           |___|__________|___|

c --- initial parameters
c      t1 = 0.1d0
c      t2 = 0.2d0
c      t3 = 0.3d0
c      t4 = 0.4d0
      
      ElemPerSide = 4
      ElemThruThick = 1
      numNodes = 72
      NodeThruThick = 2*ElemThruThick + 1
      numElems = numElems1
      NodePerSide = NodePerSide1
c --- define element connectivity
      data  ((ElemConn1(kj,ki),kj=1,NodePerSide1),ki=1,numElems1) /
     &        1,3,21,19,2,12,20,10,11,
     &        3,5,23,21,4,14,22,12,13,
     &        5,7,25,23,6,16,24,14,15,
     &        7,9,27,25,8,18,26,16,17,
     &        28,30,48,46,29,39,47,37,38,
     &        30,32,50,48,31,41,49,39,40,
     &        32,34,52,50,33,43,51,41,42,
     &        34,36,54,52,35,45,53,43,44,
     &        19,21,63,61,20,57,62,55,56,
     &        61,63,30,28,62,69,29,67,68,
     &        25,27,66,64,26,60,65,58,59,
     &        64,66,36,34,65,72,35,70,71/

      call vi_move(ElemConn1(1,1),ElemConn(1,1),numElems1*NodePerSide1)
      
c --- flag all elements as real
      do k1 = 1,numElems
         rData(2*numNodes+k1) = ONE
      enddo
      
c --- define node locations 
c --- y-locations first along bottom
      PosIncr = HALF * t1 / (dble(ElemThruThick))
      ii = 1
      rData(ii) = ZERO
      do k1 = 1,2*ElemThruThick
         ii = ii + 1
         rData(ii) = rData(ii-1) + PosIncr
      enddo
      PosIncr = FOURTH * (w1-t1-t2) / (dble(ElemThruThick))
      do k1 = 1,4*ElemThruThick
         ii = ii+1
         rData(ii) = rData(ii-1) + PosIncr
      enddo
      PosIncr = HALF * t2 / (dble(ElemThruThick))
      do k1 = 1,2*ElemThruThick
         ii = ii + 1
         rData(ii) = rData(ii-1)+PosIncr
      enddo


c --- copy y-locations up thru section (areas 1 to 6)
      do k1 = 2,2*NodeThruThick
         do k2 = 1,NodePerSide
            ii = ii + 1
            rData(ii) = rData(k2)
         enddo
      enddo
c --- copy y-locations up thru section (areas 7 to 8)
      do k1 = NodeThruThick+1,2*NodeThruThick
         do k2 = 1,NodePerSide
            if (k2.gt.NodeThruThick .and. 
     &          k2.le.NodePerSide-NodeThruThick) cycle
            ii = ii + 1
            rData(ii) = rData(k2)
         enddo
      enddo

c --- z-locations along left edge
      ii = 1
      rDataz(ii) = ZERO
      PosIncr = HALF * t3 / (dble(ElemThruThick))
      do k1 = 1,2*ElemThruThick
         ii = ii + 1
         rDataZ(ii) = rDataZ(ii-1)+PosIncr
      enddo
      PosIncr = FOURTH * (w2-t3-t4) / (dble(ElemThruThick))
      do k1 = 1,4*ElemThruThick
         ii = ii + 1
         rDataZ(ii)=rDataZ(ii-1)+PosIncr
      enddo
      PosIncr = HALF * t4 / (dble(ElemThruThick))
      do k1 = 1,2*ElemThruThick
         ii = ii + 1
         rDataZ(ii) = rDataZ(ii-1)+PosIncr
      enddo
c --- copy z-locations across thru section (areas 1 to 3)
      ii = numNodes
      do k1 = 1,NodeThruThick
         do k2 = 1,NodePerSide
            ii = ii + 1
            rData(ii) = rDataz(k1)
         enddo
      enddo
c --- copy z-locations across thru section (areas 4 to 6)
      do k1 = NodePerSide-NodeThruThick+1,NodePerSide
         do k2 = 1,NodePerSide
            ii = ii + 1
            rData(ii) = rDataz(k1)
         enddo
      enddo
c --- copy z-locations across thru section (areas 7 to 8)
      do k1 = NodeThruThick+1,2*NodeThruThick
         do k2 = 1,NodePerSide
            if (k2.gt.NodeThruThick .and. 
     &          k2.le.NodePerSide-NodeThruThick) cycle
            ii = ii + 1
            rData(ii) = rDataz(k1)
         enddo
      enddo

      return
      end


